package org.molgenis.charts.highcharts.data;

public class Point
{
	private String id;
	private String name;
	private Number x;
	private Number y;
	
	public String getId()
	{
		return id;
	}
	public void setId(String id)
	{
		this.id = id;
	}
	public String getName()
	{
		return name;
	}
	public void setName(String name)
	{
		this.name = name;
	}
	public Number getX()
	{
		return x;
	}
	public void setX(Number x)
	{
		this.x = x;
	}
	public Number getY()
	{
		return y;
	}
	public void setY(Number y)
	{
		this.y = y;
	}

}
